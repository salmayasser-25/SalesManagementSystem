    private void btnCompleteSaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCompleteSaleActionPerformed
        // TODO add your handling code here:
           
        String selectedCustomerName = (String) cmbCustomer.getSelectedItem();
        if (selectedCustomerName == null) {
            JOptionPane.showMessageDialog(this, "Please select a customer");
            return;
        }


        Customer selected = null;
        List<Customer> customers = customerDAO.getAllCustomers();
        for (Customer c : customers) {
            if (c.getCustomerName().equals(selectedCustomerName)) {
                selected = c;
                break;
            }
        }

        if (selected == null) {
            JOptionPane.showMessageDialog(this, "Customer not found");
            return;
        }

        if (cartItems.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Cart is empty");
            return;
        }

        double total = Double.parseDouble(txtTotal.getText());
        Sale sale = new Sale(selected.getCustomerId(), total);

        if (saleDAO.saveSale(sale, cartItems)) {
            JOptionPane.showMessageDialog(this, "Sale completed successfully!");
            cartItems.clear();
            updateCartTable();
            calculateTotal();
            loadProducts();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to complete sale!");
        }
    }//GEN-LAST:event_btnCompleteSaleActionPerformed

