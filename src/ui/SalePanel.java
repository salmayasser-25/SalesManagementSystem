    private void btnAddProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductActionPerformed
        // TODO add your handling code here:
    String selectedProductName = (String) cmbProduct.getSelectedItem();
    if (selectedProductName == null) {
        JOptionPane.showMessageDialog(this, "Please select a product");
        return;
    }
    
    
    Product selected = null;
    List<Product> products = productDAO.getAllProducts();
    for (Product p : products) {
        if (p.getProductName().equals(selectedProductName)) {
            selected = p;
            break;
        }
    }
    
    if (selected == null) {
        JOptionPane.showMessageDialog(this, "Product not found");
        return;
    }
    
    int qty;
    try {
        qty = Integer.parseInt(txtQuantity.getText());
        if (qty <= 0) {
            JOptionPane.showMessageDialog(this, "Quantity must be greater than 0");
            return;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter a valid quantity");
        return;
    }
    
    if (qty > selected.getQuantity()) {
        JOptionPane.showMessageDialog(this, "Not enough stock! Available: " + selected.getQuantity());
        return;
    }
    
    SaleDetail detail = new SaleDetail(selected.getProductId(), qty, selected.getPrice());
    detail.setProductName(selected.getProductName());
    cartItems.add(detail);
    updateCartTable();
    calculateTotal();
    txtQuantity.setText("");
    }
    
    private void updateCartTable() {
        cartModel.setRowCount(0);
        for (SaleDetail d : cartItems) {
            cartModel.addRow(new Object[]{d.getProductId(), d.getProductName(), d.getQuantity(), d.getUnitPrice(), d.getSubtotal()});
        }
    }
    
    private void calculateTotal() {
        double total = 0;
        for (SaleDetail d : cartItems) {
            total += d.getSubtotal();
        }
        txtTotal.setText(String.format("%.2f", total));
    }//GEN-LAST:event_btnAddProductActionPerformed

