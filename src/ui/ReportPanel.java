         private void loadAllSales() {
        tableModel.setRowCount(0);
        List<Sale> sales = saleDAO.getAllSales();
        for (Sale s : sales) {
            tableModel.addRow(new Object[]{
                s.getSaleId(),
                s.getCustomerName(),
                dateFormat.format(s.getSaleDate()),
                s.getTotalAmount()
            });
        }
        calculateTotal();
        //calculateStatistics();
    }
        private void loadSalesByDateRange(java.util.Date from, java.util.Date to) {
        tableModel.setRowCount(0);
        List<Sale> sales = saleDAO.getSalesByDateRange(from, to);
        for (Sale s : sales) {
            tableModel.addRow(new Object[]{
                s.getSaleId(),
                s.getCustomerName(),
                dateFormat.format(s.getSaleDate()),
                s.getTotalAmount()
            });
        }
        calculateTotal();
        
        
        if (tableModel.getRowCount() == 0) {
            lblHighest.setText("0.00");
            lblLowest.setText("0.00");
            lblAverage.setText("0.00");
            return;
        }
        
        double max = (double) tableModel.getValueAt(0, 3);
        double min = (double) tableModel.getValueAt(0, 3);
        double sum = 0;
        
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            double amount = (double) tableModel.getValueAt(i, 3);
            if (amount > max) max = amount;
            if (amount < min) min = amount;
            sum += amount;
        }
        
        double avg = sum / tableModel.getRowCount();
        
        lblHighest.setText(String.format("%.2f", max));
        lblLowest.setText(String.format("%.2f", min));
        lblAverage.setText(String.format("%.2f", avg));
    }
        private void calculateTotal() {
        double total = 0;
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            total += (double) tableModel.getValueAt(i, 3);
        }
        txtTotalSales.setText(String.format("%.2f", total));
    }
            private void calculateStatistics() {
        List<Sale> sales = saleDAO.getAllSales();
        if (sales.isEmpty()) {
            lblHighest.setText("0.00");
            lblLowest.setText("0.00");
            lblAverage.setText("0.00");
            return;
        }
        
        double max = sales.get(0).getTotalAmount();
        double min = sales.get(0).getTotalAmount();
        double sum = 0;
        
        for (Sale s : sales) {
            double amount = s.getTotalAmount();
            if (amount > max) max = amount;
            if (amount < min) min = amount;
            sum += amount;
        }
        
        double avg = sum / sales.size();
        
        lblHighest.setText(String.format("%.2f", max));
        lblLowest.setText(String.format("%.2f", min));
        lblAverage.setText(String.format("%.2f", avg));
    }

