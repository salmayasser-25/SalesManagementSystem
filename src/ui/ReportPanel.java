    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        try {
            java.util.Date fromDate = sdf.parse(txtFromDate.getText().trim());
            java.util.Date toDate = sdf.parse(txtToDate.getText().trim());
            
            if (fromDate.after(toDate)) {
                JOptionPane.showMessageDialog(this, "From date must be before To date");
                return;
            }
            
            loadSalesByDateRange(fromDate, toDate);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please enter valid dates (yyyy-MM-dd)");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

